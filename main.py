word = random.randint(0, len(word_list) - 1)
chosen_word = word_list[word]







# creating display empty list in order to have _ as many as letters chosen word have
display = []
for letter in chosen_word:
    display.append("_")
#number of lives
lives = 6
# loop
while True:
    #ask the user guess letter
    guess = input("Guess a letter: ").lower()
    # if user has already mentioned letter before
    if guess in display:
        print("You've already guessed that letter.")
        continue
    # replacing _ with letter if guessed correctly
    for i in range(0, len(chosen_word)):
        letter = chosen_word[i]
        if letter == guess:
            display[i] = guess
    # reducing number of lives if user guess wrong
    if guess not in display:
        lives -= 1
        print("You guessed wrong, my dear")
    # if user guessed all letters correctly then loop breaks and announces winner
    if display == list(chosen_word):
        print(f"You guessed the word {chosen_word}! Well done, my friend.")
        break
    # if user runs out of lives then loop breaks and user lose
    if lives == 0:
        print(stages[lives])
        print(f"Sorry to say but you lost! The word was {chosen_word}. Next time, pal")
        break
    # showing the stages of hangman
    print(stages[lives])
    # shows the joint part of everything
    print("".join(display))
